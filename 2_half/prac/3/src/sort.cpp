//--------------------------------------------------------------------------------------------------------------------------------
// bubble_sort
//--------------------------------------------------------------------------------------------------------------------------------

void bubble_sort(int *const arr, const size_t n)
{
    //log_verify(arr != nullptr, ;);

    for (size_t i = 0; i < n; ++i)
    {
        for (size_t j = 0; j < n - (i + 1); ++j)
        {
            if (arr[j] > arr[j + 1]) int_swap(arr + j, arr + (j + 1));
        }
    }
}

//--------------------------------------------------------------------------------------------------------------------------------
// insert_sort
//--------------------------------------------------------------------------------------------------------------------------------

void insert_sort(int *const arr, const size_t n)
{
    //log_verify(arr != nullptr, ;);

    for (size_t i = 1; i < n; ++i)
    {
        for (size_t j = i; j > 0; --j)
        {
            if (arr[j] > arr[j - 1]) break;

            int_swap(arr + j, arr + (j - 1));
        }
    }
}

//--------------------------------------------------------------------------------------------------------------------------------
// choose_sort
//--------------------------------------------------------------------------------------------------------------------------------

void choose_sort(int *const arr, const size_t n)
{
    //log_verify(arr != nullptr, ;);
    //log_verify(n > 0, ;);

    for (size_t i = 0; i < n; ++i)
    {
        for (size_t j = i + 1; j < n; ++j)
        {
            if (arr[j] < arr[i]) int_swap(arr + j, arr + i);
        }
    }
}

//--------------------------------------------------------------------------------------------------------------------------------
// swap
//--------------------------------------------------------------------------------------------------------------------------------

void int_swap(int *const a, int *const b)
{
    //log_verify(a != nullptr, ;);
    //log_verify(b != nullptr, ;);

    int temp = *a;
    *a = *b;
    *b = temp;
}